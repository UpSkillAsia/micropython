def scroll(value, speed=0.06):
    value = str(value)
    value = bytearray(value)
    buffer = bytearray(5 + (len(value) * 5) + 5)
    for i in range(5):
        buffer[i] = 0
    for x in range(len(value)):
        for i in range(6):
            buffer[i + 5 + (x * 5)] = font_pendolino3[(((value[x] - ASCII_START) * 5) + i)]
    for i in range(5):
        buffer[5 + (len(value) * 5) + i] = 0
    for i in range(1 + len(value)):
        rawBuffer = bytearray(b'\x00' * 5)
        for x in range(5):
            for y in range(5):
                rawBuffer[y] = (buffer[(i * 5) + y] << x) & 0x1F | (buffer[((i + 1) * 5) + y] & 0x1F) >> (5 - x)
            raw(rawBuffer)
            sleep(speed)
    clear()

def clear():
    global displayBuff
    raw(b'\x00' * 5)
    displayBuff = bytearray(16)

def show2x5(value):
    global displayBuff
    displayBuff = bytearray(16)
    value = str(value).upper()
    value = bytearray(value)
    value = value[:3]
    value = value[:(3 if b'.' in value else 2)]
    nextIndex = 0
    if (len(value) < (3 if b'.' in value else 2)): # fit to right
        nextIndex = nextIndex + (((5 if b'.' in value else 4) - len(value)) * 4)
    showDotFlag = False
    for x in range(len(value)):
        c = value[x]
        charIndex = 0
        if c >= ord(b'0') and c <= ord(b'9'):
            charIndex = c - ord(b'0')
        elif c == ord(b'-'):
            charIndex = 10
        elif c == ord(b'.'):
            showDotFlag = True
            continue
        else:
            nextIndex = nextIndex + 4
            continue
        for i in range(4):
            displayBuff[nextIndex] = font2x5[((charIndex * 4) + i)] | (0x04 if showDotFlag else 0)
            if showDotFlag: 
                showDotFlag = False
            nextIndex = nextIndex + 1
    raw(displayBuff)

def plot(value):
    global displayBuff
    for i in range(5):
        displayBuff[i] = (displayBuff[i] << 1) & 0x1F
    value = int(value)
    if value >= 0 and value <= 4:
        displayBuff[4 - value] = displayBuff[4 - value] | 0x01
    raw(displayBuff)

def dot(x, y, value):
    global displayBuff
    if value:
        displayBuff[y] = displayBuff[y] | (0x10 >> x)
    else:
        displayBuff[y] = displayBuff[y] & ((0x10 >> x) ^ 0x1F)
    raw(displayBuff)


np = NeoPixel(Pin(13, Pin.OUT), 25); 
np.bright = 10

def raw(data, color=(255, 0, 0)):
    i = 0
    for row in range(5):
        for col in range(5):
            np[i] = (255, 0, 0) if data[row] & (0x10 >> col) else (0, 0, 0)
            i = i + 1
    np.write()

